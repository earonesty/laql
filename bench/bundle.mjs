import { execFile } from "node:child_process";
import { mkdir, readFile, rm, writeFile } from "node:fs/promises";
import { join } from "node:path";
import { fileURLToPath } from "node:url";
import { promisify } from "node:util";

const repoRoot = fileURLToPath(new URL("..", import.meta.url));
const outDir = join(repoRoot, "bench/generated/bundle");
const reportPath = join(repoRoot, "bench/BUNDLE.md");
const execFileAsync = promisify(execFile);

const entries = [
  {
    name: "parquet-only",
    file: "parquet-only.js",
    source: `
      import { createParquetLake } from "lakeql-parquet";
      import { memoryStore } from "lakeql-core";
      export function makeLake() {
        return createParquetLake({ store: memoryStore() });
      }
    `,
    forbidden: [
      "apache-arrow",
      "tableFromArrays",
      "createCsvLake",
      "readCsvObjects",
      "createJsonLake",
      "readJsonObjects",
      "@turf/boolean-contains",
      "@turf/boolean-intersects",
      "h3-js",
    ],
  },
  {
    name: "arrow-output",
    file: "arrow-output.js",
    source: `
      import { rowsToArrowIPC } from "lakeql-arrow";
      export function encode(rows) {
        return rowsToArrowIPC(rows);
      }
    `,
    required: ["tableToIPC"],
  },
  {
    name: "csv-ingest",
    file: "csv-ingest.js",
    source: `
      import { readCsvObjects } from "lakeql-csv";
      export async function read(text) {
        return readCsvObjects(text);
      }
    `,
    required: ["readCsvObjects"],
    forbidden: ["apache-arrow", "tableFromArrays"],
  },
  {
    name: "json-ingest",
    file: "json-ingest.js",
    source: `
      import { readJsonObjects } from "lakeql-json";
      export async function read(text) {
        return readJsonObjects(text);
      }
    `,
    required: ["readJsonObjects"],
    forbidden: ["apache-arrow", "tableFromArrays"],
  },
];

await rm(outDir, { recursive: true, force: true });
await mkdir(outDir, { recursive: true });

const results = [];
for (const entry of entries) {
  const entryPath = join(outDir, entry.file);
  const entryOutDir = join(outDir, entry.name);
  const outputPath = join(entryOutDir, entry.file);
  const metafilePath = join(entryOutDir, "meta.json");
  await mkdir(entryOutDir, { recursive: true });
  await writeFile(entryPath, entry.source);
  await execFileAsync("pnpm", [
    "exec",
    "esbuild",
    entryPath,
    "--bundle",
    "--format=esm",
    "--platform=browser",
    "--splitting=true",
    "--tree-shaking=true",
    "--packages=bundle",
    "--main-fields=browser,module,main",
    "--conditions=browser,import,default",
    `--outdir=${entryOutDir}`,
    `--metafile=${metafilePath}`,
  ]);
  const js = await readText(outputPath);
  const metafile = JSON.parse(await readText(metafilePath));
  const forbiddenHits = hits(js, entry.forbidden ?? []);
  const missingRequired = misses(js, entry.required ?? []);
  if (forbiddenHits.length > 0 || missingRequired.length > 0) {
    throw new Error(
      `${entry.name} bundle contract failed: forbidden=${forbiddenHits.join(",") || "none"} missing=${
        missingRequired.join(",") || "none"
      }`,
    );
  }
  const bytes = Buffer.byteLength(js);
  results.push({
    name: entry.name,
    bytes,
    kib: bytes / 1024,
    inputs: Object.keys(metafile.inputs).length,
    forbidden: entry.forbidden ?? [],
    required: entry.required ?? [],
  });
}

await writeFile(reportPath, renderReport(results));
console.log(renderConsole(results));
console.log(`wrote ${reportPath}`);

async function readText(path) {
  return readFile(path, "utf8");
}

function hits(text, patterns) {
  return patterns.filter((pattern) => text.includes(pattern));
}

function misses(text, patterns) {
  return patterns.filter((pattern) => !text.includes(pattern));
}

function renderConsole(results) {
  console.table(
    results.map((result) => ({
      entry: result.name,
      "bundle KiB": result.kib.toFixed(1),
      inputs: result.inputs,
    })),
  );
  return "";
}

function renderReport(results) {
  const headers = ["Entry", "Bundle KiB", "Inputs", "Required Symbols", "Forbidden Symbols"];
  const rows = results.map((result) => [
    result.name,
    result.kib.toFixed(1),
    String(result.inputs),
    result.required.join(", ") || "-",
    result.forbidden.join(", ") || "-",
  ]);
  return `# Bundle Contract Benchmarks

Generated by \`pnpm bench:bundle\` on ${new Date().toISOString().slice(0, 10)}.

This benchmark is a static browser-bundle check for the parity plan's smart-load rule:
optional CSV, JSON, Arrow, and exact geo dependencies must not enter the Parquet-only
bundle. It is not a minified production-size claim; it is a regression guard for the
static dependency graph.

${markdownTable(headers, rows)}
`;
}

function markdownTable(headers, rows) {
  return [
    `| ${headers.join(" | ")} |`,
    `| ${headers.map(() => "---").join(" | ")} |`,
    ...rows.map((row) => `| ${row.join(" | ")} |`),
  ].join("\n");
}
